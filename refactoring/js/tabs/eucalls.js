// ── Comparison table ──
function buildComparisonTable(yearsDisplay) {
  const card = document.getElementById('ec-comparisonTable')?.closest('.card');
  if (card) card.style.display = yearsDisplay.length < 2 ? 'none' : '';
  const table = document.getElementById('ec-comparisonTable');
  let html = '<thead><tr><th>Metric</th>';
  yearsDisplay.forEach(y => { html += `<th>${y.year}</th>`; });
  html += '</tr></thead><tbody>';
  [
    ['Projects',        y => y.statistics.total_projects],
    ['Participants',    y => y.statistics.total_participants],
    ['Call Budget',     y => fmtCurrency(extractCallBudget(y))],
    ['Project Budget',  y => fmtCurrency(y.statistics.total_budget)],
    ['EU Contribution', y => fmtCurrency(y.statistics.total_eu_contribution)]
  ].forEach(([label, fn]) => {
    html += `<tr><td><strong>${label}</strong></td>`;
    yearsDisplay.forEach(y => { html += `<td>${fn(y)}</td>`; });
    html += '</tr>';
  });
  html += '</tbody>';
  table.innerHTML = html;
}

function extractCallBudget(yearData) {
  const ci = yearData.callInfo;
  if (!ci || !ci.budget_overview) return 0;
  const bm  = ci.budget_overview.budgetTopicActionMap || {};
  const tid = yearData.topic || '';
  let total = 0;
  for (const aid in bm) {
    (bm[aid] || []).forEach(action => {
      if ((action.action || '').toUpperCase().includes(tid.toUpperCase())) {
        Object.values(action.budgetYearMap || {}).forEach(v => { total += parseFloat(v) || 0; });
      }
    });
  }
  return total;
}

