// ── CONFIG ──
const YEARS_TO_TRY = [2027, 2026, 2025, 2024, 2023, 2022, 2021];
const SEARCH_API   = 'https://api.tech.ec.europa.eu/search-api/prod/rest/search';
const DOC_API      = 'https://api.tech.ec.europa.eu/search-api/prod/rest/document';
const PROJECTS_API = 'https://ec.europa.eu/info/funding-tenders/opportunities/api/topicProjectsList.json';
const CACHE_KEY    = 'edf_calls_v1';
const CACHE_TTL_MS = 6 * 3600 * 1000; // 6 hours → triggers silent background refresh
const PAGE_SIZE    = 50;

// ── LOCAL STATE ──
let currentData      = null;
let currentPattern   = null;
let currentYears     = [];
