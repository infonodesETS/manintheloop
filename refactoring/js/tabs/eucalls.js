// MAIN SEARCH HANDLER
// ══════════════════════════════════════════════════════════════════════════════
async function handleSearch() {
  const raw = document.getElementById('ec-topicInput').value.trim().toUpperCase();
  if (!raw) { alert('Please enter a topic identifier'); return; }

  if (!raw.startsWith('EDF-')) {
    showEdfOnlyModal();
    return;
  }

  currentData = null; currentPattern = null; currentYears = [];
  ecHide('ec-resultsSection');
  ecHide('ec-errorSection');
  ecClearLog();
  closeDrop();

  const pattern = extractPattern(raw);
  if (!pattern) {
    showError('Invalid format. Use: EDF-YYYY-SUFFIX (e.g. EDF-2024-RA-SI-MATCOMP-EC-STEP)');
    return;
  }

  setLoading(true);
  ecShow('ec-progressSection');
  setProgress(5, `Pattern found: ${pattern.base}`);

  // ── Fast path: serve from edf_calls.json if projects already fetched ──────
  if (edfCallsDb?.[raw]?.projects?.length > 0) {
    const cached = edfCallsDb[raw];
    ecLog(`Loaded from file (${cached.projects.length} project(s))`, 'ok');
    const yearData = buildYearDataFromCache(raw, cached);
    const found    = [{ year: pattern.year, data: yearData }];
    setProgress(100, 'Done!');
    currentPattern = pattern;
    currentYears   = found;
    currentData    = {
      pattern,
      years: found,
      summary: {
        total_years:        1,
        years_list:         [pattern.year],
        total_projects:     yearData.statistics.total_projects,
        total_participants: yearData.statistics.total_participants,
      }
    };
    setTimeout(() => { ecHide('ec-progressSection'); renderResults(); setLoading(false); }, 300);
    return;
  }

  const found = [];

  // If the identifier includes a specific year, only search that year.
  // Otherwise (wildcard / no year) try all years.
  const yearsToSearch = pattern.year ? [parseInt(pattern.year, 10)] : YEARS_TO_TRY;

  for (let i = 0; i < yearsToSearch.length; i++) {
    const year = yearsToSearch[i];
    const pct  = 10 + Math.round(((i + 1) / yearsToSearch.length) * 80);
    setProgress(pct, `Searching year ${year}… (${i + 1}/${yearsToSearch.length})`);
    ecLog(`Searching ${pattern.prefix}-${year}-${pattern.suffix}…`, 'info');

    try {
      const yearData = await scrapeYear(pattern.prefix, year, pattern.suffix);
      if (yearData) {
        found.push({ year: String(year), data: yearData });
        const np = yearData.statistics.total_projects;
        ecLog(`Year ${year}: found – ${np} project${np === 1 ? '' : 's'}`, 'ok');
      } else {
        ecLog(`Year ${year}: no data`, 'warn');
      }
    } catch (err) {
      ecLog(`Year ${year}: error – ${err.message}`, 'err');
    }
  }

  if (found.length === 0) {
    setLoading(false);
    ecHide('ec-progressSection');
    showError(`No data found for pattern "${pattern.base}" in years 2021–2027.`);
    return;
  }

  found.sort((a, b) => parseInt(b.year) - parseInt(a.year));
  setProgress(100, 'Done!');

  const totalProjects = found.reduce((s, y) => s + y.data.statistics.total_projects, 0);
  const totalPart     = found.reduce((s, y) => s + y.data.statistics.total_participants, 0);

  currentPattern = pattern;
  currentYears   = found;
  currentData    = {
    pattern,
    years: found,
    summary: {
      total_years:        found.length,
      years_list:         found.map(y => y.year),
      total_projects:     totalProjects,
      total_participants: totalPart
    }
  };

  setTimeout(() => {
    ecHide('ec-progressSection');
    renderResults();
    setLoading(false);
  }, 600);
}

// ══════════════════════════════════════════════════════════════════════════════
// SCRAPE SINGLE YEAR
// ══════════════════════════════════════════════════════════════════════════════
async function scrapeYear(prefix, year, suffix) {
  const topicIdentifier = `${prefix}-${year}-${suffix}`;

  // Use JSON file data if available (avoids unreliable text search)
  const cached = edfCallsDb?.[topicIdentifier];
  let topicId, topicDescription, budgetOverview;

  if (cached?.ccm2Id) {
    topicId          = cached.ccm2Id;
    topicDescription = cached.description || null;
    budgetOverview   = cached.budget_overview || null;
    ecLog(`  Using ccm2Id from file: ${topicId}`, 'info');
  } else {
    const meta = await fetchTopicMeta(topicIdentifier);
    if (!meta) return null;
    ({ topicId, topicDescription, budgetOverview } = meta);
  }

  let projects = [];
  try {
    const resp = await fetch(`${PROJECTS_API}?topicId=${encodeURIComponent(topicId)}`);
    if (resp.ok) {
      const raw = await resp.json();
      projects = Array.isArray(raw) ? raw : [];
    }
  } catch (e) {
    ecLog(`  Could not load project list for ${topicIdentifier}: ${e.message}`, 'warn');
  }

  const processed = projects.map(p => ({
    TITLE:        p.title || '',
    ACRONYM:      p.acronym || '',
    'PROJECT ID': p.businessIdentifier || '',
    URL:          `https://ec.europa.eu/info/funding-tenders/opportunities/portal/screen/opportunities/projects-details/${p.programId}/${p.businessIdentifier}`,
    _endpoint:    `${DOC_API}/${p.programId}${p.businessIdentifier}?apiKey=SEDIA_NONH2020_PROD`
  }));

  const detailed = [];
  for (let i = 0; i < processed.length; i++) {
    try { detailed.push(await fetchProjectDetails(processed[i])); }
    catch (_) { detailed.push(processed[i]); }
    if (i < processed.length - 1) await sleep(800);
  }

  const updates = await fetchTopicUpdates(topicIdentifier);

  let totalBudget = 0, totalEU = 0, totalPart = 0;
  for (const p of detailed) {
    totalBudget += parseFloat(p['Overall budget']  || 0) || 0;
    totalEU     += parseFloat(p['EU contribution'] || 0) || 0;
    totalPart   += (p.Participants || []).length;
  }

  return {
    success:            true,
    topic_identifier:   topicIdentifier,
    scraping_timestamp: new Date().toISOString(),
    call_info:          { topic_id: topicId, topic_description: topicDescription, budget_overview: budgetOverview },
    topic_updates:      { total_updates: updates.length, updates },
    statistics:         { total_projects: detailed.length, total_participants: totalPart, total_budget: totalBudget, total_eu_contribution: totalEU },
    projects:           detailed
  };
}

