async function fetchTopicMeta(topicIdentifier) {
  const query = {
    bool: { must: [{ terms: { type: ['1', '2', '8'] } }, { term: { identifier: topicIdentifier } }] }
  };
  // Use the suffix only as search text (drops EDF-YYYY prefix) to avoid
  // year-bucket competition that pushes the exact doc out of top results.
  const suffix = topicIdentifier.replace(/^EDF-\d{4}-/i, '') || topicIdentifier;
  const url  = `${SEARCH_API}?apiKey=SEDIA&text=${encodeURIComponent(suffix)}`;
  const body = new URLSearchParams({ query: JSON.stringify(query), pageNumber: 1, pageSize: 50 });

  let resp;
  try { resp = await proxiedFetch(url, { method: 'POST', body }); }
  catch (e) { throw new Error(`Search API unreachable: ${e.message}`); }

  if (!resp.ok) return null;
  const data    = await resp.json();
  const results = data.results || [];
  if (results.length === 0) return null;

  ecLog(`  API returned ${results.length} result(s) for ${topicIdentifier}`, 'info');
  results.slice(0, 3).forEach(r => {
    const ids   = r.metadata?.identifier || r.metadata?.topicCode || [];
    const idStr = Array.isArray(ids) ? ids.join(', ') : ids;
    ecLog(`    → ref: ${r.reference || '?'} | identifier: ${idStr}`, 'info');
  });

  const idUpper = topicIdentifier.toUpperCase();
  const match   = results.find(r => {
    const ids   = r.metadata?.identifier || r.metadata?.topicCode || [];
    const idArr = Array.isArray(ids) ? ids : [ids];
    return idArr.some(id => (id || '').toUpperCase() === idUpper);
  });
  if (!match) return null;

  const meta    = match.metadata || {};
  const ccm2Id  = meta.ccm2Id;
  const topicId = ccm2Id ? (Array.isArray(ccm2Id) ? ccm2Id[0] : ccm2Id) : null;
  if (!topicId) return null;

  const descArr          = meta.descriptionByte;
  const topicDescription = descArr && descArr.length > 0 ? descArr[0] : null;

  let budgetOverview = null;
  const budgetRaw = meta.budgetOverview;
  if (budgetRaw && budgetRaw.length > 0) {
    try { budgetOverview = JSON.parse(budgetRaw[0]); } catch (_) {}
  }

  return { topicId, topicDescription, budgetOverview };
}

async function fetchProjectDetails(project) {
  const resp = await proxiedFetch(project._endpoint);
  if (!resp.ok) return project;

  const data = await resp.json();
  const meta = data.metadata || {};
  const get1 = (k, def = '') => { const v = meta[k]; return (v && v.length > 0) ? v[0] : def; };

  project.Status             = get1('status');
  project['Start date']      = get1('startDate');
  project['End date']        = get1('endDate');
  project['Overall budget']  = normBudget(get1('overallBudget'));
  project['EU contribution'] = normBudget(get1('euContributionAmount'));
  project.Objective          = get1('objective');
  project['Type of action']  = get1('typeOfAction');

  const partJson   = get1('participants', '[]');
  let participants = [];
  try {
    participants = JSON.parse(partJson).map(p => {
      const cc      = p.postalAddress?.countryCode;
      const country = (cc && typeof cc === 'object') ? cc.description : (cc || '');
      const pic     = String(p.pic || '');
      return {
        'Organization Name': p.legalName || '',
        PIC:                 pic,
        Role:                p.role || '',
        Country:             country,
        'EU Contribution':   normBudget(p.eucontribution || ''),
        Status:              p.status || '',
        eu_url:              pic ? `https://ec.europa.eu/info/funding-tenders/opportunities/portal/screen/how-to-participate/org-details/${pic}` : '',
      };
    });
  } catch (_) {}
  project.Participants = participants;
  return project;
}

async function fetchTopicUpdates(topicIdentifier) {
  const query = {
    bool: { must: [{ terms: { type: ['6'] } }, { term: { identifier: topicIdentifier } }] }
  };
  try {
    const url  = `${SEARCH_API}?apiKey=SEDIA&text=${encodeURIComponent(topicIdentifier)}`;
    const body = new URLSearchParams({ query: JSON.stringify(query) });
    const resp = await proxiedFetch(url, { method: 'POST', body });
    if (!resp.ok) return [];
    const data    = await resp.json();
    return (data.results || []).map(u => {
      const m = u.metadata || {};
      return {
        reference:   u.reference || null,
        date:        (m.startDate   || [null])[0],
        title:       (m.title       || [null])[0],
        description: (m.description || [null])[0],
        language:    (m.language    || [null])[0]
      };
    }).sort((a, b) => (b.date || '') > (a.date || '') ? 1 : -1);
  } catch (_) { return []; }
}

