function extractPattern(identifier) {
  const m = identifier.match(/^(.+?)-(\d{4})-(.+)$/);
  if (!m) return null;
  const [, prefix, year, suffix] = m;
  return { base: `${prefix}-${suffix}`, year, prefix, suffix };
}

function normBudget(val) {
  if (!val) return '';
  const s = String(val).replace(/[€$,\s]/g, '');
  return isNaN(parseFloat(s)) ? '' : s;
}

function fmtCurrency(val) {
  if (!val && val !== 0) return '-';
  const n = parseFloat(String(val).replace(/[€$,\s]/g, ''));
  if (isNaN(n) || n === 0) return '-';
  return new Intl.NumberFormat('it-IT', { style: 'currency', currency: 'EUR', maximumFractionDigits: 0 }).format(n);
}

function fmtBudgetShort(val) {
  const n = parseFloat(val);
  if (!n || isNaN(n)) return '-';
  return (n / 1e6).toFixed(1) + 'M €';
}

function fmtDate(ds) {
  if (!ds) return '-';
  try { return new Date(ds).toLocaleDateString('en-GB', { year: 'numeric', month: '2-digit', day: '2-digit' }); }
  catch (_) { return ds; }
}

function escHtml(text) {
  if (!text) return '';
  return String(text).replace(/[&<>"']/g, c => (
    { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#039;' }[c]
  ));
}

function getStatusClass(status) {
  if (!status) return 'badge-default';
  const s = status.toLowerCase();
  if (s.includes('ongoing'))    return 'badge-ongoing';
  if (s.includes('closed') || s.includes('ended')) return 'badge-closed';
  if (s.includes('forthcoming')) return 'badge-forth';
  return 'badge-default';
}

