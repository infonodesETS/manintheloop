function showDropFiltered(rawQuery) {
  if (!acReady) return;
  const q    = rawQuery.trim().toLowerCase();
  const base = activeList();

  const matches = q.length === 0
    ? base
    : base.filter(c =>
        c.identifier.toLowerCase().includes(q) ||
        c.title.toLowerCase().includes(q) ||
        c.desc.includes(q)
      );

  if (matches.length === 0) { closeDrop(); return; }
  renderDrop(matches.slice(0, 50), rawQuery.trim()); // cap at 50 visible items
}

function initAutocomplete() {
  const input = document.getElementById('ec-topicInput');
  const drop  = document.getElementById('ec-callDrop');
  if (!input || !drop) return;

  // Show all calls on focus (click into field)
  input.addEventListener('focus', () => {
    showDropFiltered(input.value);
  });

  // Filter on typing
  input.addEventListener('input', () => {
    showDropFiltered(input.value);
  });

  // Keyboard navigation
  input.addEventListener('keydown', e => {
    if (!drop.classList.contains('open')) return;

    const items   = [...drop.querySelectorAll('.live-drop-item')];
    const current = drop.querySelector('.live-drop-item.ec-active');
    const idx     = current ? items.indexOf(current) : -1;

    if (e.key === 'ArrowDown') {
      e.preventDefault();
      items.forEach(i => i.classList.remove('ec-active'));
      (items[idx + 1] || items[0]).classList.add('ec-active');
    } else if (e.key === 'ArrowUp') {
      e.preventDefault();
      items.forEach(i => i.classList.remove('ec-active'));
      (items[idx - 1] || items[items.length - 1]).classList.add('ec-active');
    } else if (e.key === 'Enter') {
      const active = drop.querySelector('.live-drop-item.ec-active');
      if (active) {
        e.preventDefault();
        selectCall(active.dataset.id, input);
      }
    } else if (e.key === 'Escape') {
      closeDrop();
    }
  });

  // Close on outside click
  document.addEventListener('click', e => {
    if (!e.target.closest('#ec-topicInput') && !e.target.closest('#ec-callDrop')) {
      closeDrop();
    }
  });
}

function renderDrop(matches, rawQuery) {
  const drop = document.getElementById('ec-callDrop');
  if (!drop) return;

  const q = rawQuery.toLowerCase();

  drop.innerHTML = matches.map(c => {
    const statusDot = c.status
      ? `<span class="ec-status-dot ec-status-${statusClass(c.status)}"></span>`
      : '';
    const deadlineStr = c.deadline
      ? ` · <span style="opacity:.5">${fmtDate(c.deadline)}</span>`
      : '';
    return `
    <div class="live-drop-item" data-id="${escHtml(c.identifier)}">
      <div class="live-label">${highlight(escHtml(c.title || '—'), q)}</div>
      <div class="live-desc">${highlight(escHtml(c.identifier), q)} ${statusDot}${deadlineStr}</div>
    </div>`;
  }).join('');

  drop.querySelectorAll('.live-drop-item').forEach(item => {
    item.addEventListener('mousedown', e => {
      e.preventDefault(); // prevent input blur before click fires
      selectCall(item.dataset.id, document.getElementById('ec-topicInput'));
    });
  });

  drop.classList.add('open');
}

function selectCall(identifier, input) {
  input.value = identifier;
  closeDrop();
  acEnterBlocked = false;
}

function closeDrop() {
  const drop = document.getElementById('ec-callDrop');
  if (drop) { drop.classList.remove('open'); drop.innerHTML = ''; }
}

/** Wraps matching substrings in <mark> for highlighting */
function highlight(text, query) {
  if (!query || query.length < 2) return text;
  try {
    const re = new RegExp(`(${query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
    return text.replace(re, '<mark>$1</mark>');
  } catch (_) { return text; }
}

function statusClass(s) {
  if (!s) return 'unknown';
  const l = s.toLowerCase();
  if (l.includes('open'))        return 'open';
  if (l.includes('forthcoming')) return 'forthcoming';
  if (l.includes('closed'))      return 'closed';
  return 'unknown';
}

