/** Build a scrapeYear-compatible result object from a cached edf_calls.json entry. */
function buildYearDataFromCache(identifier, cached) {
  const projects = (cached.projects || []).map(p => ({
    TITLE:             p.title        || '',
    ACRONYM:           p.acronym      || '',
    'PROJECT ID':      p.project_id   || '',
    URL:               p.url          || '',
    Status:            p.status       || '',
    'Start date':      p.start_date   || '',
    'End date':        p.end_date     || '',
    'Overall budget':  p.overall_budget   || '',
    'EU contribution': p.eu_contribution  || '',
    Objective:         p.objective    || '',
    'Type of action':  p.type_of_action || '',
    Participants: (p.participants || []).map(part => ({
      'Organization Name': part.organization_name  || '',
      PIC:                 part.pic                || '',
      Role:                part.role               || '',
      Country:             part.country            || '',
      'EU Contribution':   part.eu_contribution    || '',
      Status:              part.status             || '',
      // enriched fields from fetch_edf_bulk.py
      eu_url:              part.eu_url             || '',
      activity_type:       part.activity_type      || '',
      organization_type:   part.organization_type  || '',
      type:                part.type               || '',
      sme:                 part.sme,
      country_code:        part.country_code       || '',
      city:                part.city               || '',
      postal_code:         part.postal_code        || '',
      street:              part.street             || '',
      latitude:            part.latitude           || '',
      longitude:           part.longitude          || '',
      web_link:            part.web_link           || '',
      order:               part.order,
    })),
  }));

  let totalBudget = 0, totalEU = 0, totalPart = 0;
  for (const p of projects) {
    totalBudget += parseFloat(p['Overall budget']  || 0) || 0;
    totalEU     += parseFloat(p['EU contribution'] || 0) || 0;
    totalPart   += (p.Participants || []).length;
  }

  return {
    success:            true,
    topic_identifier:   identifier,
    scraping_timestamp: cached._projects_fetched_at || cached._fetched_at || new Date().toISOString(),
    call_info:          { topic_id: cached.ccm2Id || '', topic_description: cached.description || null, budget_overview: cached.budget_overview || null },
    topic_updates:      { total_updates: 0, updates: [] },
    statistics:         { total_projects: projects.length, total_participants: totalPart, total_budget: totalBudget, total_eu_contribution: totalEU },
    projects,
  };
}

