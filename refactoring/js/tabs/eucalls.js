// ══════════════════════════════════════════════════════════════════════════════
// CORS PROXY CHAIN
// ══════════════════════════════════════════════════════════════════════════════
async function proxiedFetch(url, options = {}) {
  const isPost = (options.method || 'GET').toUpperCase() === 'POST';
  const chain = isPost
    ? [u => u,
       u => `https://corsproxy.io/?url=${encodeURIComponent(u)}`]
    : [u => u,
       u => `https://api.allorigins.win/raw?url=${encodeURIComponent(u)}`,
       u => `https://corsproxy.io/?url=${encodeURIComponent(u)}`];

  for (const mkUrl of chain) {
    try {
      const r = await fetch(mkUrl(url), options);
      if (r.status !== 403 && r.status !== 429 && r.status !== 408) return r;
    } catch (_) {}
  }
  throw new Error('All CORS proxies failed');
}

// ══════════════════════════════════════════════════════════════════════════════
// CALLS LIST CACHE  (localStorage + stale-while-revalidate)
// ══════════════════════════════════════════════════════════════════════════════

function cacheRead() {
  try {
    const raw = localStorage.getItem(CACHE_KEY);
    if (!raw) return null;
    const obj = JSON.parse(raw);
    return (obj?.data?.length && obj?.timestamp) ? obj : null;
  } catch (_) { return null; }
}

function cacheWrite(data) {
  try {
    localStorage.setItem(CACHE_KEY, JSON.stringify({ data, timestamp: Date.now() }));
  } catch (_) {}
}

/**
 * Load the EDF calls list.
 * Priority: edf_calls.json (static file) → localStorage cache → live API fetch.
 * force=true skips the file and cache, goes straight to API (used by ↺ refresh).
 */
async function loadEdfCalls(force = false) {
  // ── 1. Try static file (generated by fetch_edf_bulk.py) ──────────────────
  if (!force) {
    try {
      const resp = await fetch('data/edf_calls.json');
      if (resp.ok) {
        const json = await resp.json();
        if (json?.calls && Object.keys(json.calls).length > 0) {
          edfCallsDb = json.calls;
          const list = Object.values(json.calls).map(c => ({
            identifier: c.identifier,
            title:      c.title       || '',
            status:     c.status      || '',
            deadline:   c.deadline    || '',
            desc:       (c.description || '').slice(0, 600).toLowerCase(),
          }));
          applyCallsList(list);
          const ts = json._generated_at ? new Date(json._generated_at).getTime() : null;
          setCallStatus('file', ts, `${list.length} calls from file`);
          // Populate header file date
          const fileDateEl = document.getElementById('ec-fileDate');
          if (fileDateEl && json._generated_at) {
            const d = new Date(json._generated_at);
            fileDateEl.textContent = d.toLocaleDateString('en-GB', { day:'2-digit', month:'2-digit', year:'numeric' });
          }
          // Background delta check for new calls
          deltaCheck().catch(() => {});
          return;
        }
      }
    } catch (_) {}

    // ── 2. Fall back to localStorage cache ───────────────────────────────────
    const cached = cacheRead();
    if (cached) {
      applyCallsList(cached.data);
      setCallStatus('ready', cached.timestamp);
      if (Date.now() - cached.timestamp > CACHE_TTL_MS) {
        fetchAllEdfTopics().then(data => {
          if (data.length > 0) { cacheWrite(data); applyCallsList(data); setCallStatus('ready', Date.now()); }
        }).catch(() => {});
      }
      return;
    }
  }

  // ── 3. Live API fetch ─────────────────────────────────────────────────────
  setCallStatus('fetching');
  try {
    const data = await fetchAllEdfTopics();
    if (data.length > 0) {
      cacheWrite(data);
      applyCallsList(data);
      setCallStatus('ready', Date.now());
    } else {
      setCallStatus('empty');
    }
  } catch (err) {
    const cached = cacheRead();
    if (cached) {
      applyCallsList(cached.data);
      setCallStatus('stale', cached.timestamp);
    } else {
      setCallStatus('error', null, err.message);
    }
  }
}

/**
 * Lightweight delta check: fetch one page of the latest year from the API
 * and flag any identifiers not present in edfCallsDb.
 */
async function deltaCheck() {
  if (!edfCallsDb) return;
  try {
    const query = { bool: { must: [{ terms: { type: ['1', '2', '8'] } }] } };
    const url   = `${SEARCH_API}?apiKey=SEDIA&text=${encodeURIComponent('EDF-' + YEARS_TO_TRY[0])}`;
    const body  = new URLSearchParams({ query: JSON.stringify(query), pageNumber: 1, pageSize: PAGE_SIZE });
    const resp  = await proxiedFetch(url, { method: 'POST', body });
    if (!resp.ok) return;
    const results = (await resp.json()).results || [];
    const newCalls = [];
    for (const r of results) {
      const ids = r.metadata?.identifier || [];
      const id  = (Array.isArray(ids) ? ids[0] : ids) || '';
      if (id && id.toUpperCase().startsWith('EDF-') && !edfCallsDb[id.toUpperCase()]) {
        newCalls.push(id.toUpperCase());
      }
    }
    if (newCalls.length > 0) {
      const el = document.getElementById('ec-callStatusText');
      if (el) {
        el.innerHTML += ` · <span style="color:#e3b341;font-weight:700">⚠ ${newCalls.length} new call${newCalls.length > 1 ? 's' : ''} not in file — run fetch_edf_bulk.py --update</span>`;
      }
    }
  } catch (_) {}
}

function applyCallsList(data) {
  edfCallsList = data;
  acReady = true;
  updateHeaderCounts();
  showDropFiltered('');
}

function updateHeaderCounts() {
  const totalEl   = document.getElementById('ec-totalCallsCount');
  const fundedEl  = document.getElementById('ec-fundedCount');
  if (totalEl) totalEl.textContent = edfCallsList.length;
  if (fundedEl) {
    const n = edfCallsDb ? Object.values(edfCallsDb).filter(c => c.projects?.length > 0).length : 0;
    fundedEl.textContent = n ? `(${n})` : '';
  }
}

/** Returns the subset of edfCallsList shown in the dropdown based on current filter. */
function activeList() {
  if (!fundedOnly || !edfCallsDb) return edfCallsList;
  return edfCallsList.filter(c => (edfCallsDb[c.identifier]?.projects?.length ?? 0) > 0);
}

// ── Fetch all EDF topics from the Search API (paginated, year-by-year) ──
// KEY: the API is 1-indexed — pageNumber=1 is the first page, 0 returns nothing.
// pageNumber and pageSize go in the POST body (not the URL).
// We scan up to MAX_PAGES per year and stop early if a page adds 0 new topics.
async function fetchAllEdfTopics() {
  const query    = { bool: { must: [{ terms: { type: ['1', '2', '8'] } }] } };
  const all      = [];
  const seen     = new Set();
  const MAX_PAGES = 8;   // up to 400 results per year before dedup
  const PAGE_SIZE = 50;

  for (const year of YEARS_TO_TRY) {
    let newThisYear = 0;

    for (let page = 1; page <= MAX_PAGES; page++) {  // ← 1-indexed!
      const url  = `${SEARCH_API}?apiKey=SEDIA&text=${encodeURIComponent('EDF-' + year)}`;
      const body = new URLSearchParams({
        query:      JSON.stringify(query),
        pageNumber: page,     // ← 1-indexed, in body
        pageSize:   PAGE_SIZE // ← in body
      });

      let resp;
      try {
        resp = await proxiedFetch(url, { method: 'POST', body });
      } catch (e) {
        console.warn(`[EU Calls] year ${year} p${page}:`, e.message);
        break;
      }
      if (!resp.ok) { console.warn(`[EU Calls] year ${year} p${page} HTTP ${resp.status}`); break; }

      const results = (await resp.json()).results || [];
      let newThisPage = 0;

      for (const r of results) {
        const meta       = r.metadata || {};
        const idArr      = meta.identifier || [];
        const rawId      = (Array.isArray(idArr) ? idArr[0] : idArr) || '';
        if (!rawId) continue;
        const identifier = String(rawId).toUpperCase();
        if (!identifier.startsWith('EDF-')) continue;
        if (seen.has(identifier)) continue;
        seen.add(identifier);
        newThisPage++;

        const get1 = k => { const v = meta[k] || []; return (Array.isArray(v) ? v[0] : v) || ''; };
        all.push({
          identifier,
          title:    get1('title') || get1('topicTitle'),
          status:   get1('status'),
          deadline: get1('deadlineDate') || get1('submissionDeadlineDate')
        });
      }

      console.log(`[EU Calls] ${year} p${page}: ${results.length} results, ${newThisPage} new → total ${all.length}`);

      // Update the autocomplete list progressively so the dropdown is usable
      // even before the full fetch is done
      applyCallsList(all.slice());
      setCallStatus('loading_progress', null, `${all.length} calls found so far…`);

      // Stop early: no new topics on this page, or short page (last page)
      if (newThisPage === 0 || results.length < PAGE_SIZE) break;
      newThisYear += newThisPage;

      await sleep(250);
    }

    await sleep(300);
  }

  console.log(`[EU Calls] done — ${all.length} unique EDF topics`);

  all.sort((a, b) => {
    const ya = parseInt((a.identifier.match(/\d{4}/) || ['0'])[0]);
    const yb = parseInt((b.identifier.match(/\d{4}/) || ['0'])[0]);
    if (yb !== ya) return yb - ya;
    return a.identifier.localeCompare(b.identifier);
  });

  return all;
}

// ── Status bar ──
function setCallStatus(state, timestamp = null, errMsg = '') {
  const el = document.getElementById('ec-callStatusText');
  if (!el) return;

  const refresh = `<a href="javascript:void(0)" id="ec-refreshLink" style="color:var(--accent)">↺ refresh</a>`;

  switch (state) {
    case 'file': {
      const n   = edfCallsList.length;
      const ago = timeSince(timestamp);
      el.innerHTML = `<span style="color:var(--accent);font-weight:700">${n}</span> calls · file ${ago} · ${refresh}`;
      break;
    }
    case 'fetching':
      el.innerHTML = '<span style="opacity:.6">Fetching call list from EU Portal…</span>';
      break;
    case 'loading_progress':
      el.innerHTML = `<span style="opacity:.6">${escHtml(errMsg)}</span>`;
      break;
    case 'ready': {
      const n   = edfCallsList.length;
      const ago = timeSince(timestamp);
      el.innerHTML = `<span style="color:var(--accent);font-weight:700">${n}</span> calls loaded · updated ${ago} · ${refresh}`;
      break;
    }
    case 'stale': {
      const n   = edfCallsList.length;
      const ago = timeSince(timestamp);
      el.innerHTML = `<span style="opacity:.6">${n} calls (cached ${ago}, fetch failed) · ${refresh}</span>`;
      break;
    }
    case 'empty':
      el.innerHTML = `No EDF calls found in API. · ${refresh}`;
      break;
    case 'error':
      el.innerHTML = `<span style="color:#f85149">Could not load call list: ${escHtml(errMsg)}</span> · ${refresh}`;
      break;
  }

  // Wire refresh link (re-created each time innerHTML changes)
  const link = document.getElementById('ec-refreshLink');
  if (link) {
    link.addEventListener('click', () => {
      edfCallsList = [];
      acReady = false;
      loadEdfCalls(true);
    });
  }
}

function timeSince(ts) {
  if (!ts) return '?';
  const diff = Date.now() - ts;
  const mins = Math.floor(diff / 60000);
  if (mins < 1)  return 'just now';
  if (mins < 60) return `${mins}m ago`;
  const hrs = Math.floor(mins / 60);
  if (hrs < 24)  return `${hrs}h ago`;
  return `${Math.floor(hrs / 24)}d ago`;
}

